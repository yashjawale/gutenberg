/**
 * Generate the token-delimited API reference content for a block.
 *
 * @param {string} blockDir Directory name.
 * @return {string} Markdown content wrapped in START/END TOKEN delimiters.
 */
function generateBlockApiSection( blockDir ) {
	const blockJson = readBlockJson( blockDir );
	const files = getBlockFiles( blockDir );
	const blockType = getBlockType( files, blockJson );

	const {
		name,
		category,
		description,
		keywords,
		apiVersion,
		attributes,
		supports,
		usesContext,
		providesContext,
		styles,
		selectors,
		__experimental: experimental,
	} = blockJson;

	const slug = name.replace( 'core/', '' );
	const lines = [];

	// Deprecation notice replaces the description for deprecated blocks.
	if ( description.startsWith( 'This block is deprecated.' ) ) {
		const replacement = description
			.replace( 'This block is deprecated. ', '' )
			.replace( 'This block is deprecated.', '' )
			.trim();
		const suffix = replacement ? ` ${ replacement }` : '';
		lines.push(
			`<div class="callout callout-alert">This block is <strong>deprecated</strong> and should not be used in new content.${ suffix }</div>`
		);
		lines.push( '' );
	} else {
		// Experimental notice.
		if ( experimental ) {
			lines.push(
				'<div class="callout callout-warning">This block is <strong>experimental</strong> and may change or be removed without notice.</div>'
			);
			lines.push( '' );
		}

		if ( description ) {
			lines.push( description );
			lines.push( '' );
		}
	}

	// Metadata.
	lines.push( `- **Name:** \`${ name }\`` );
	lines.push(
		`- **Category:** [${ category }](https://developer.wordpress.org/block-editor/reference-guides/core-blocks/core-blocks-${ category }/)`
	);
	if ( apiVersion ) {
		lines.push(
			`- **API Version:** [${ apiVersion }](https://developer.wordpress.org/block-editor/reference-guides/block-api/block-api-versions/)`
		);
	}

	const RENDERING_GUIDE =
		'https://developer.wordpress.org/block-editor/getting-started/fundamentals/static-dynamic-rendering/';
	const typeLabel = {
		static: `[Static](${ RENDERING_GUIDE }) (saved in post content)`,
		dynamic: `[Dynamic](${ RENDERING_GUIDE }) (server-rendered)`,
		hybrid: `[Hybrid](${ RENDERING_GUIDE }) (static save + server enhancements)`,
	};
	lines.push( `- **Block Type:** ${ typeLabel[ blockType ] || 'Unknown' }` );

	if ( keywords && keywords.length > 0 ) {
		lines.push(
			`- **Keywords:** ${ keywords
				.map( ( k ) => `\`${ k }\`` )
				.join( ', ' ) }`
		);
	}

	lines.push( '' );

	// Block relationships.
	const relationships = formatRelationships( blockJson );
	if ( relationships ) {
		lines.push( '## Block Relationships' );
		lines.push( '' );
		lines.push( relationships );
		lines.push( '' );
	}

	// Attributes.
	lines.push( '## Attributes' );
	lines.push( '' );
	lines.push(
		'_Defined via the [`attributes`](https://developer.wordpress.org/block-editor/reference-guides/block-api/block-attributes/) property in block.json._'
	);
	lines.push( '' );
	lines.push( formatAttributesTable( attributes ) );
	lines.push( '' );

	// Supports.
	lines.push( '## Supports' );
	lines.push( '' );
	lines.push(
		'_Defined via the [`supports`](https://developer.wordpress.org/block-editor/reference-guides/block-api/block-supports/) property in block.json._'
	);
	lines.push( '' );
	lines.push( formatSupports( supports ) );
	lines.push( '' );

	// Context.
	const contextSection = formatContext( usesContext, providesContext );
	if ( contextSection ) {
		lines.push( '## Context' );
		lines.push( '' );
		lines.push(
			'_Defined via the [`usesContext` and `providesContext`](https://developer.wordpress.org/block-editor/reference-guides/block-api/block-context/) properties in block.json._'
		);
		lines.push( '' );
		lines.push( contextSection );
		lines.push( '' );
	}

	// Styles.
	const stylesSection = formatStyles( styles );
	if ( stylesSection ) {
		lines.push( '## Block Styles' );
		lines.push( '' );
		lines.push(
			'_Defined via the [`styles`](https://developer.wordpress.org/block-editor/reference-guides/block-api/block-styles/) property in block.json._'
		);
		lines.push( '' );
		lines.push( stylesSection );
		lines.push( '' );
	}

	// Selectors.
	const selectorsSection = formatSelectors( selectors );
	if ( selectorsSection ) {
		lines.push( '## CSS Selectors' );
		lines.push( '' );
		lines.push(
			'_Defined via the [`selectors`](https://developer.wordpress.org/block-editor/reference-guides/block-api/block-selectors/) property in block.json._'
		);
		lines.push( '' );
		lines.push( selectorsSection );
		lines.push( '' );
	}

	// Block markup example.
	lines.push( '## Block Markup' );
	lines.push( '' );
	if ( blockType === 'dynamic' ) {
		lines.push(
			'This is a [**dynamic block**](https://developer.wordpress.org/block-editor/getting-started/fundamentals/static-dynamic-rendering/). It is rendered on the server and does not save HTML in post content.'
		);
		lines.push( '' );
		lines.push( 'In post content, it is stored as a block comment:' );
	} else if ( blockType === 'hybrid' ) {
		lines.push(
			'This is a [**hybrid block**](https://developer.wordpress.org/block-editor/getting-started/fundamentals/static-dynamic-rendering/). It saves static markup that the server may enhance during rendering.'
		);
	} else {
		lines.push(
			'This is a [**static block**](https://developer.wordpress.org/block-editor/getting-started/fundamentals/static-dynamic-rendering/). The markup is saved directly in the post content.'
		);
	}
	lines.push( '' );

	const fixtureFile = `core__${ blockDir }.html`;
	const fixturePath = path.join( FIXTURES_DIR, fixtureFile );
	if ( fs.existsSync( fixturePath ) ) {
		lines.push( '```html' );
		lines.push( fs.readFileSync( fixturePath, 'utf-8' ).trimEnd() );
		lines.push( '```' );
	} else {
		lines.push( '```html' );
		lines.push(
			generateBlockCommentExample( slug, attributes, blockType )
		);
		lines.push( '```' );
	}
	lines.push( '' );

	// Source files reference.
	lines.push( '## Source' );
	lines.push( '' );
	lines.push(
		`- [block.json](${ SOURCE_URL_BASE }${ blockDir }/block.json) ([reference](https://developer.wordpress.org/block-editor/reference-guides/block-api/block-metadata/))`
	);
	lines.push(
		`- [Source directory](${ SOURCE_URL_BASE }${ blockDir }/) — browse \`edit.js\`, \`save.js\`, \`index.php\`, and more.`
	);
	lines.push( '' );

	return `${ TOKEN_START }\n${ lines.join( '\n' ) }\n${ TOKEN_END }\n`;
}

/**
 * Write a block's README.md with token-delimited generated content.
 *
 * - File doesn't exist → create with H1 title + description + tokens.
 * - File exists, no tokens → append tokens at the end.
 * - File exists, has tokens → replace content between tokens.
 *
 * @param {string} blockDir   Directory name inside block-library/src.
 * @param {string} apiSection Token-wrapped generated content.
 */
function writeBlockReadme( blockDir, apiSection ) {
	const readmePath = path.join( BLOCK_LIBRARY_DIR, blockDir, 'README.md' );
	const blockJson = readBlockJson( blockDir );
	const title =
		blockJson.title ||
		blockDir
			.split( '-' )
			.map( ( w ) => w[ 0 ].toUpperCase() + w.slice( 1 ) )
			.join( ' ' );

	if ( ! fs.existsSync( readmePath ) ) {
		// Case 1: No README — create full file.
		const header = `# ${ title }\n\n`;
		fs.writeFileSync( readmePath, header + apiSection, {
			encoding: 'utf8',
		} );
		return;
	}

	const existing = fs.readFileSync( readmePath, 'utf8' );

	if ( existing.includes( TOKEN_START ) ) {
		// Case 3: Tokens exist — replace content between them.
		const tokenPattern = new RegExp(
			escapeRegExp( TOKEN_START ) +
				'[\\s\\S]*?' +
				escapeRegExp( TOKEN_END ) +
				'\\n?',
			'm'
		);
		fs.writeFileSync(
			readmePath,
			existing.replace( tokenPattern, apiSection ),
			{ encoding: 'utf8' }
		);
		return;
	}

	// Case 2: File exists but no tokens — append at the end.
	const separator = existing.endsWith( '\n' ) ? '\n' : '\n\n';
	fs.writeFileSync( readmePath, existing + separator + apiSection, {
		encoding: 'utf8',
	} );
}

/**
 * Escape a string for use in a RegExp.
 *
 * @param {string} str
 * @return {string} Escaped string safe for RegExp use.
 */
function escapeRegExp( str ) {
	return str.replace( /[.*+?^${}()|[\]\\]/g, '\\$&' );
}

/**
 * Generate the token-delimited block list for a category index page.
 *
 * @param {string}   category Category slug.
 * @param {string[]} blocks   Block directory names in this category.
 * @return {string} Token-wrapped markdown block list.
 */
function generateCategoryPageSection( category, blocks ) {
	const lines = [];

	blocks.forEach( ( blockDir ) => {
		const blockJson = readBlockJson( blockDir );
		const { title, name, description } = blockJson;
		const catSlug = `core-blocks-${ category }`;
		const blockSlug = `core-block-${ blockDir }`;
		const blockUrl = `https://developer.wordpress.org/block-editor/reference-guides/core-blocks/${ catSlug }/${ blockSlug }/`;
		lines.push(
			`- [${ title }](${ blockUrl }) — \`${ name }\`${
				description ? ': ' + description : ''
			}`
		);
	} );

	lines.push( '' );

	return `${ TOKEN_START }\n${ lines.join( '\n' ) }\n${ TOKEN_END }\n`;
}

/**
 * Write a category index page with token-delimited generated content.
 *
 * Follows the same three-case logic as writeBlockReadme:
 * - File doesn't exist → create with H1 title + tokens.
 * - File exists, has tokens → replace content between tokens.
 * - File exists, no tokens → append tokens at the end.
 *
 * @param {string} category Category slug.
 * @param {string} label    Human-readable category label.
 * @param {string} section  Token-wrapped generated content.
 */
function writeCategoryPage( category, label, section ) {
	const filePath = path.join(
		CATEGORY_DOCS_DIR,
		`category-${ category }.md`
	);

	if ( ! fs.existsSync( filePath ) ) {
		fs.writeFileSync( filePath, `# ${ label } Blocks\n\n${ section }`, {
			encoding: 'utf8',
		} );
		return;
	}

	const existing = fs.readFileSync( filePath, 'utf8' );

	if ( existing.includes( TOKEN_START ) ) {
		const tokenPattern = new RegExp(
			escapeRegExp( TOKEN_START ) +
				'[\\s\\S]*?' +
				escapeRegExp( TOKEN_END ) +
				'\\n?',
			'm'
		);
		fs.writeFileSync( filePath, existing.replace( tokenPattern, section ), {
			encoding: 'utf8',
		} );
		return;
	}

	const separator = existing.endsWith( '\n' ) ? '\n' : '\n\n';
	fs.writeFileSync( filePath, existing + separator + section, {
		encoding: 'utf8',
	} );
}

/**
 * Generate the deprecated-blocks notice section for the core blocks README.
 *
 * @param {string[]} dirs Deprecated block directory names.
 * @return {string} Token-wrapped markdown section.
 */
function generateDeprecatedNotice( dirs ) {
	const lines = [];

	lines.push(
		'**Deprecated blocks** — the following blocks are deprecated and should not be used in new content.'
	);
	lines.push( '' );

	dirs.forEach( ( blockDir ) => {
		const { title, name, description, category } =
			readBlockJson( blockDir );
		const replacement = description
			.replace( 'This block is deprecated. ', '' )
			.replace( 'This block is deprecated.', '' )
			.trim();
		const note = replacement ? ` — ${ replacement }` : '';
		const url = `https://developer.wordpress.org/block-editor/reference-guides/core-blocks/core-blocks-${ category }/core-block-${ blockDir }/`;
		lines.push( `- [${ title }](${ url }) (\`${ name }\`)${ note }` );
	} );

	lines.push( '' );

	return (
		DEPRECATED_TOKEN_START +
		'\n' +
		lines.join( '\n' ) +
		'\n' +
		DEPRECATED_TOKEN_END +
		'\n'
	);
}

/**
 * Write the deprecated-blocks notice into the core blocks README,
 * replacing the content between the deprecated token pair.
 *
 * @param {string[]} dirs Deprecated block directory names.
 */
function writeDeprecatedNotice( dirs ) {
	const content = fs.readFileSync( CORE_BLOCKS_README, 'utf-8' );
	const tokenPattern = new RegExp(
		escapeRegExp( DEPRECATED_TOKEN_START ) +
			'[\\s\\S]*?' +
			escapeRegExp( DEPRECATED_TOKEN_END ) +
			'\\n?',
		'm'
	);
	fs.writeFileSync(
		CORE_BLOCKS_README,
		content.replace( tokenPattern, generateDeprecatedNotice( dirs ) ),
		{ encoding: 'utf8' }
	);
}

