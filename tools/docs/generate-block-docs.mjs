/**
 * Discover all block directories that contain a block.json.
 *
 * @return {string[]} Sorted list of directory names.
 */
function getBlockDirs() {
	return fs
		.readdirSync( BLOCK_LIBRARY_DIR, { withFileTypes: true } )
		.filter(
			( d ) =>
				d.isDirectory() &&
				fs.existsSync(
					path.join( BLOCK_LIBRARY_DIR, d.name, 'block.json' )
				)
		)
		.map( ( d ) => d.name )
		.sort();
}

/**
 * Return true for deprecated blocks, identified by their description starting
 * with "This block is deprecated." — the consistent convention used across all
 * deprecated core blocks.
 *
 * @param {string} blockDir Directory name inside block-library/src.
 * @return {boolean} Whether the block is deprecated.
 */
function isDeprecated( blockDir ) {
	const { description = '' } = readBlockJson( blockDir );
	return description.startsWith( 'This block is deprecated.' );
}

/**
 * Read and parse a block.json file.
 *
 * @param {string} blockDir Directory name inside block-library/src.
 * @return {Object} Parsed block.json contents.
 */
function readBlockJson( blockDir ) {
	const filePath = path.join( BLOCK_LIBRARY_DIR, blockDir, 'block.json' );
	return JSON.parse( fs.readFileSync( filePath, 'utf-8' ) );
}

/**
 * Check which source files exist for a block.
 *
 * @param {string} blockDir Directory name.
 * @return {Object} Flags for each possible source file.
 */
function getBlockFiles( blockDir ) {
	const dir = path.join( BLOCK_LIBRARY_DIR, blockDir );
	return {
		hasSaveJs: fs.existsSync( path.join( dir, 'save.js' ) ),
		hasIndexPhp: fs.existsSync( path.join( dir, 'index.php' ) ),
	};
}

// ─── Anchor validation ──────────────────────────────────────────────────────

/**
 * Slugify a Markdown heading into an anchor the way the WordPress handbook
 * renderer does: lowercase, strip markup escapes, convert dots and spaces
 * to dashes, remove everything else that isn't alphanumeric, a dash, or
 * an underscore.
 *
 * @param {string} heading Raw heading text (without the leading `#` marks).
 * @return {string} Slug suitable for use as an anchor.
 */
function slugifyHeading( heading ) {
	return heading
		.toLowerCase()
		.trim()
		.replace( /\\\\/g, '' ) // strip markdown backslash escapes
		.replace( /[.\s]+/g, '-' ) // dots and spaces → dashes
		.replace( /[^\w-]/g, '' ); // drop everything else
}

/**
 * Read a markdown doc file, extract heading anchors, and error if any
 * expected anchor is missing. Sets process.exitCode = 1 so CI fails when
 * a handbook heading is renamed and the generator's anchor map drifts.
 *
 * @param {string}   docPath         Absolute path to the markdown file.
 * @param {string[]} expectedAnchors Anchors the generator relies on.
 * @param {string}   label           Short name for error messages.
 */
function validateDocAnchors( docPath, expectedAnchors, label ) {
	if ( ! fs.existsSync( docPath ) ) {
		console.error( `✖  Cannot validate anchors: ${ docPath } not found.` );
		process.exitCode = 1;
		return;
	}

	const content = fs.readFileSync( docPath, 'utf-8' );
	const anchors = new Set(
		Array.from( content.matchAll( /^#{1,6}\s+(.+)$/gm ), ( m ) =>
			slugifyHeading( m[ 1 ] )
		)
	);

	for ( const anchor of expectedAnchors ) {
		if ( ! anchors.has( anchor ) ) {
			console.error(
				`✖  Anchor "#${ anchor }" not found in ${ label }. ` +
					`Update the corresponding map in generate-block-docs.mjs.`
			);
			process.exitCode = 1;
		}
	}
}

// ─── Formatting helpers ─────────────────────────────────────────────────────

/**
 * Build a markdown link to a specific anchor on the Block Attributes docs page.
 *
 * @param {string} label  Display text for the link.
 * @param {string} anchor Key in ATTRIBUTE_ANCHORS.
 * @return {string} Markdown link.
 */
function attrLink( label, anchor ) {
	return `[${ label }](${ ATTRIBUTES_REF }#${ ATTRIBUTE_ANCHORS[ anchor ] })`;
}

/**
 * Format attributes as a Markdown table.
 *
 * @param {Object} attributes
 * @return {string} Markdown table or placeholder text.
 */
function formatAttributesTable( attributes ) {
	if ( ! attributes || Object.keys( attributes ).length === 0 ) {
		return '_This block has no custom attributes._';
	}

	const rows = [
		`| Attribute | ${ attrLink( 'Type', 'type' ) } | ${ attrLink(
			'Default',
			'default'
		) } | Description |`,
		'|-----------|------|---------|-------------|',
	];

	for ( const [ attrName, attrDef ] of Object.entries( attributes ) ) {
		const type = Array.isArray( attrDef.type )
			? attrDef.type.join( ' \\| ' )
			: attrDef.type || 'N/A';
		const defaultVal =
			attrDef.default !== undefined
				? `\`${ JSON.stringify( attrDef.default ) }\``
				: '—';

		const descParts = [];

		// Simple key-value description fields.
		const simpleFields = [
			[ 'source', 'Source' ],
			[ 'selector', 'Selector' ],
			[ 'attribute', 'HTML attr' ],
			[ 'role', 'Role' ],
		];
		for ( const [ field, label ] of simpleFields ) {
			if ( attrDef[ field ] ) {
				descParts.push(
					`${ attrLink( label, field ) }: \`${ attrDef[ field ] }\``
				);
			}
		}

		// Enum needs special formatting (list of values).
		if ( attrDef.enum ) {
			descParts.push(
				`${ attrLink( 'Enum', 'enum' ) }: ${ attrDef.enum
					.map( ( v ) => `\`${ v }\`` )
					.join( ', ' ) }`
			);
		}

		const desc = descParts.length > 0 ? descParts.join( '. ' ) : '—';
		rows.push(
			`| \`${ attrName }\` | \`${ type }\` | ${ defaultVal } | ${ desc } |`
		);
	}

	return rows.join( '\n' );
}

/**
 * Build a link to the Block Supports reference for a given property.
 *
 * Only sub-properties that have their own heading on the supports page
 * get a dedicated anchor. All others fall back to the parent property anchor.
 *
 * @param {string} property Top-level or dotted property name.
 * @return {string} Absolute handbook path with anchor.
 */
const SUPPORTS_BASE =
	'https://developer.wordpress.org/block-editor/reference-guides/block-api/block-supports/';
/**
 * All anchors documented in block-supports.md, derived by parsing every
 * heading at startup so links stay in sync as the handbook evolves.
 */
const SUPPORTS_ANCHORS = fs.existsSync( SUPPORTS_DOC_PATH )
	? new Set(
			Array.from(
				fs
					.readFileSync( SUPPORTS_DOC_PATH, 'utf-8' )
					.matchAll( /^#{1,6}\s+(.+)$/gm ),
				( m ) => slugifyHeading( m[ 1 ] )
			)
	  )
	: new Set();

/**
 * Build a link to the Block Supports reference for a given property.
 *
 * Returns null when the property has no matching anchor in block-supports.md
 * so that callers can render plain code instead of a broken link.
 *
 * @param {string} property Top-level or dotted sub-property name.
 * @return {string|null} Absolute handbook URL with anchor, or null.
 */
function supportsLink( property ) {
	const anchor = property.toLowerCase().replace( /\./g, '-' );
	return SUPPORTS_ANCHORS.has( anchor )
		? `${ SUPPORTS_BASE }#${ anchor }`
		: null;
}

/**
 * Format supports as a readable list.
 *
 * @param {Object} supports
 * @return {string} Markdown list or placeholder text.
 */
function formatSupports( supports ) {
	if ( ! supports || Object.keys( supports ).length === 0 ) {
		return '_This block does not declare explicit supports._';
	}

	const lines = [];

	for ( const [ key, value ] of Object.entries( supports ) ) {
		if ( key.startsWith( '__' ) ) {
			continue; // Skip experimental/unstable top-level keys in detail view.
		}
		const link = supportsLink( key );
		const keyLink = link ? `[\`${ key }\`](${ link })` : `\`${ key }\``;
		if ( typeof value === 'boolean' ) {
			lines.push( `- ${ keyLink }: \`${ value }\`` );
		} else if ( Array.isArray( value ) ) {
			lines.push(
				`- ${ keyLink }: ${ value
					.map( ( v ) => `\`${ JSON.stringify( v ) }\`` )
					.join( ', ' ) }`
			);
		} else if ( typeof value === 'object' && value !== null ) {
			const subEntries = Object.entries( value ).filter(
				( [ subKey ] ) => ! subKey.startsWith( '__' )
			);
			if ( subEntries.length === 0 ) {
				lines.push( `- ${ keyLink }: \`true\`` );
			} else {
				lines.push( `- ${ keyLink }:` );
				for ( const [ subKey, subValue ] of subEntries ) {
					const subProp = `${ key }.${ subKey }`;
					const subLink = supportsLink( subProp );
					const subLabel = subLink
						? `[\`${ subKey }\`](${ subLink })`
						: `\`${ subKey }\``;
					if ( typeof subValue === 'object' && subValue !== null ) {
						lines.push(
							`  - ${ subLabel }: \`${ JSON.stringify(
								subValue
							) }\``
						);
					} else {
						lines.push( `  - ${ subLabel }: \`${ subValue }\`` );
					}
				}
			}
		} else {
			lines.push( `- ${ keyLink }: \`${ JSON.stringify( value ) }\`` );
		}
	}

	return lines.length > 0
		? lines.join( '\n' )
		: '_This block does not declare explicit supports._';
}

/**
 * Format context information.
 *
 * @param {string[]} usesContext
 * @param {Object}   providesContext
 * @return {string|null} Markdown or null if no context.
 */
function formatContext( usesContext, providesContext ) {
	const parts = [];

	if ( usesContext && usesContext.length > 0 ) {
		parts.push( '**Uses context:**' );
		parts.push( '' );
		for ( const ctx of usesContext ) {
			parts.push( `- \`${ ctx }\`` );
		}
	}

	if ( providesContext && Object.keys( providesContext ).length > 0 ) {
		if ( parts.length > 0 ) {
			parts.push( '' );
		}
		parts.push( '**Provides context:**' );
		parts.push( '' );
		for ( const [ key, value ] of Object.entries( providesContext ) ) {
			parts.push( `- \`${ key }\` → attribute \`${ value }\`` );
		}
	}

	return parts.length > 0 ? parts.join( '\n' ) : null;
}

/**
 * Build a handbook link for a block name (e.g. `core/button`).
 *
 * Returns a markdown link if the block exists locally, otherwise plain code.
 *
 * @param {string} blockName Full block name like `core/button`.
 * @return {string} Markdown link or code span.
 */
function blockLink( blockName ) {
	const slug = blockName.replace( 'core/', '' );
	const blockJsonExists = fs.existsSync(
		path.join( BLOCK_LIBRARY_DIR, slug, 'block.json' )
	);

	if ( ! blockJsonExists ) {
		return `\`${ blockName }\``;
	}

	const { category = 'uncategorized' } = readBlockJson( slug );
	const url = `https://developer.wordpress.org/block-editor/reference-guides/core-blocks/core-blocks-${ category }/core-block-${ slug }/`;
	return `[\`${ blockName }\`](${ url })`;
}

/**
 * Format block relationships (parent, ancestor, allowedBlocks).
 *
 * @param {Object} blockJson
 * @return {string|null} Markdown or null if no relationships.
 */
function formatRelationships( blockJson ) {
	const METADATA_BASE =
		'https://developer.wordpress.org/block-editor/reference-guides/block-api/block-metadata/';
	const parts = [];

	if ( blockJson.parent && blockJson.parent.length > 0 ) {
		parts.push(
			`**[Parent](${ METADATA_BASE }#parent) blocks (direct):**`
		);
		for ( const p of blockJson.parent ) {
			parts.push( `- ${ blockLink( p ) }` );
		}
	}

	if ( blockJson.ancestor && blockJson.ancestor.length > 0 ) {
		if ( parts.length > 0 ) {
			parts.push( '' );
		}
		parts.push( `**[Ancestor](${ METADATA_BASE }#ancestor) blocks:**` );
		for ( const a of blockJson.ancestor ) {
			parts.push( `- ${ blockLink( a ) }` );
		}
	}

	if ( blockJson.allowedBlocks && blockJson.allowedBlocks.length > 0 ) {
		if ( parts.length > 0 ) {
			parts.push( '' );
		}
		parts.push(
			`**[Allowed](${ METADATA_BASE }#allowed-blocks) inner blocks:**`
		);
		for ( const b of blockJson.allowedBlocks ) {
			parts.push( `- ${ blockLink( b ) }` );
		}
	}

	return parts.length > 0 ? parts.join( '\n' ) : null;
}

/**
 * Format block styles as a table.
 *
 * @param {Array} styles
 * @return {string|null} Markdown table or null.
 */
function formatStyles( styles ) {
	if ( ! styles || styles.length === 0 ) {
		return null;
	}

	const rows = [
		'| Style Name | Label | Default |',
		'|------------|-------|---------|',
	];

	for ( const style of styles ) {
		rows.push(
			`| \`${ style.name }\` | ${ style.label } | ${
				style.isDefault ? 'Yes' : 'No'
			} |`
		);
	}

	return rows.join( '\n' );
}

/**
 * Format CSS selectors from block.json.
 *
 * @param {Object} selectors
 * @return {string|null} Markdown list or null.
 */
function formatSelectors( selectors ) {
	if ( ! selectors || Object.keys( selectors ).length === 0 ) {
		return null;
	}

	const lines = [];
	for ( const [ key, value ] of Object.entries( selectors ) ) {
		if ( typeof value === 'string' ) {
			lines.push( `- **${ key }**: \`${ value }\`` );
		} else if ( typeof value === 'object' ) {
			lines.push( `- **${ key }**:` );
			for ( const [ subKey, subValue ] of Object.entries( value ) ) {
				lines.push( `  - ${ subKey }: \`${ subValue }\`` );
			}
		}
	}
	return lines.join( '\n' );
}

/**
 * Determine block type (static, dynamic, hybrid) from source file presence
 * and block.json metadata.
 *
 * Server-side rendering is indicated by index.php (render_callback) or the
 * `render` property in block.json (typically `file:./render.php`).
 *
 * - save.js only              → static:  markup is saved in post content by the editor.
 * - server rendering only     → dynamic: markup is rendered on the server at request time.
 * - save.js + server rendering → hybrid:  editor saves static markup, server may enhance
 *   it during rendering (e.g. injecting dynamic data or wrapping with extra HTML).
 *
 * @param {Object} files     File existence flags from getBlockFiles().
 * @param {Object} blockJson Parsed block.json contents.
 * @return {string} One of 'static', 'dynamic', 'hybrid', 'unknown'.
 */
function getBlockType( files, blockJson ) {
	const hasServerRender = files.hasIndexPhp || !! blockJson.render;

	if ( files.hasSaveJs && hasServerRender ) {
		return 'hybrid';
	}
	if ( files.hasSaveJs ) {
		return 'static';
	}
	if ( hasServerRender ) {
		return 'dynamic';
	}
	return 'unknown';
}

/**
 * Generate a block comment example from block.json data.
 *
 * @param {string} slug       Block slug without core/ prefix.
 * @param {Object} attributes Block attributes definition.
 * @param {string} blockType  'static', 'dynamic', or 'hybrid'.
 * @return {string} HTML block comment example. Dynamic blocks with no
 *                  attribute defaults render a `{ \/* attributes *\/ }`
 *                  placeholder rather than a bare stub, to make the
 *                  syntactic shape visible without fabricating values.
 */
function generateBlockCommentExample( slug, attributes, blockType ) {
	const exampleAttrs = {};
	if ( attributes ) {
		for ( const [ attrName, attrDef ] of Object.entries( attributes ) ) {
			if ( attrDef.default !== undefined ) {
				exampleAttrs[ attrName ] = attrDef.default;
			}
		}
	}

	const hasAttrs = Object.keys( exampleAttrs ).length > 0;

	let attrsStr = '';
	if ( hasAttrs ) {
		attrsStr = ` ${ JSON.stringify( exampleAttrs ) }`;
	} else if ( blockType === 'dynamic' ) {
		attrsStr = ' { /* attributes */ }';
	}

	if ( blockType === 'dynamic' ) {
		return `<!-- wp:${ slug }${ attrsStr } /-->`;
	}

	return `<!-- wp:${ slug }${ attrsStr } -->\n<!-- Content... -->\n<!-- /wp:${ slug } -->`;
}

