/**
 * Per-block detail page generator.
 *
 * Generates one Markdown README.md per block inside the block's own source
 * directory (packages/block-library/src/{block}/README.md), mirroring how
 * component docs live next to their source.
 *
 * Generated content is wrapped in START/END TOKEN delimiters so that
 * hand-written content in existing READMEs is preserved.
 *
 * Category index pages are still written to
 * docs/reference-guides/core-blocks/category-{cat}.md.
 *
 * The summary page (core-blocks.md) is still generated separately
 * by gen-block-lib-list.js via the docs:blocks npm script.
 *
 * Reads from  : packages/block-library/src/{block}/block.json
 * Publishes to: packages/block-library/src/{block}/README.md
 */

